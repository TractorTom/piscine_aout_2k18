char		*ft_strlowcase(char *str)
{
	char	c;
	int		i;

	i = 0;
	c = str[0];
	while (c != '\0')
	{
		if (c >= 'A' && c <= 'Z')
			str[i] = c + 32;
		i = i + 1;
		c = str[i];
	}
	return (str);
}
