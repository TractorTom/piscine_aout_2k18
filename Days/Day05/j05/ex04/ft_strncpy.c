char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int		i;
	char	c;

	i = 0;
	c = src[i];
	while (c != '\0' && i < n)
	{
		c = src[i];
		dest[i] = c;
		i = i + 1;
	}
	return (dest);
}
