char	*ft_strcpy(char *dest, char *src)
{
	int		i;
	char	c;

	i = 0;
	c = src[i];
	while (c != '\0')
	{
		dest[i] = c;
		i = i + 1;
		c = src[i];
	}
	dest[i] = '\0';
	return (dest);
}
