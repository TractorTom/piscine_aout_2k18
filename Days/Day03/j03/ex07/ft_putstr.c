void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	char	c;
	int		i;

	c = *str;
	i = 0;
	while (c != 0)
	{
		ft_putchar(c);
		i = i + 1;
		c = *(str + i);
	}
}
