int		ft_sqrt(int nb)
{
	long i;

	if (nb < 0)
		return (0);
	else
	{
		i = 0;
		while (nb > i * i)
		{
			i = i + 1;
		}
		if (nb == i * i)
			return (i);
		else
			return (0);
	}
}
