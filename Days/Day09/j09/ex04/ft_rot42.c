char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] = 'a' + ((42 + str[i] - 'a') % 26);
		else if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = 'A' + ((42 + str[i] - 'A') % 26);
		i = i + 1;
	}
	return (str);
}
