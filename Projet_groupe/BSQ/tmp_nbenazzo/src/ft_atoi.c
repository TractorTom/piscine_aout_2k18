int	ft_atoi(char *str)
{
	int res;
	int sign;

	sign = 1;
	res = 0;
	while (*str == ' ' || *str == '\f' || *str == '\n' ||
			*str == '\t' || *str == '\v' || *str == '\r')
		str++;
	if (*str == '+' || *str == '-')
	{
		sign *= *str == '-' ? -1 : 1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res = res * 10 + *str - 48;
		str++;
	}
	return (sign * res);
}
