#include <stdio.h>
#include <string.h>

int	ft_strcmp(char *s1, char *s2)
{
	int res;
	int i;

	i = 0;
	res = 0;
	while (res == 0 && (s1[i] != '\0' && s2[i] != '\0'))
	{
		if (s1[i] != s2[i])
			res = s1[i] - s2[i];
		i++;
	}
	return (res);
}
