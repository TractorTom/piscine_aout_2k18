#include <stdio.h>

int	ft_sqrt(int nb);

int main()
{
	int	nb;

	nb = -1;
	while (nb < 1000)
	{
		printf("INT Square root of %d : %d\n", nb, ft_sqrt(nb));
		++nb;
	}
	return (0);
}
